    public boolean verificarCampos(){
        boolean verificador = true ;
        String errors = ""; 
        
        String numIdentidad = this.jTFnumIdentidad.getText();
        
        if(numIdentidad.length() != 15){
            //si el numero de identidad no tiene 15 elementos
            verificador = false;
            errors +=  " Debe ingresar correctamente el numero de identidad";
        }
        
        if(numIdentidad.charAt(4) == '-' && numIdentidad.charAt(9) != '-'){
            verificador = false;
            errors +=  " Debe ingresar correctamente el numero de identidad";
        }
        
        
            
        
        return verificador;
    }
    
