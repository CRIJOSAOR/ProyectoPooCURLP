public class CLVacunador {
    private String dniVacunador, nombresV, apellidosV, direccionV, numCelularV, estado;

    public String getDniVacunador() {
        return dniVacunador;
    }

    public void setDniVacunador(String dniVacunador) {
        this.dniVacunador = dniVacunador;
    }

    public String getNombresV() {
        return nombresV;
    }

    public void setNombresV(String nombresV) {
        this.nombresV = nombresV;
    }

    public String getApellidosV() {
        return apellidosV;
    }

    public void setApellidosV(String apellidosV) {
        this.apellidosV = apellidosV;
    }

    public String getDireccionV() {
        return direccionV;
    }

    public void setDireccionV(String direccionV) {
        this.direccionV = direccionV;
    }

    public String getNumCelularV() {
        return numCelularV;
    }

    public void setNumCelularV(String numCelular) {
        this.numCelularV = numCelularV;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
    
            
    
}
